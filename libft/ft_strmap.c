#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*c;

	if (!(c = ft_strnew(ft_strlen(s))))
		return (c);
	i = 0;
	while (s[i])
	{
		c[i] = (*f)(s[i]);
		i++;
	}
	c[i] = '\0';
	return (c);
}

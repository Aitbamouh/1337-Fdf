#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i;
	char	*c;

	i = 0;
	while (s[i])
	{
		c = &s[i];
		(f)(i, c);
		i++;
	}
}
